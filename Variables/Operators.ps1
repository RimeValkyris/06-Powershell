# Arithmetic Operators
# Addition
Write-Host "-----------------------"
$a = 5
$b = 10

$sum = $a + $b
Write-Host "Sum: $sum"  

Write-Host "-----------------------"
# Subtraction
$d = 15
$e = 7

$difference = $d - $e
Write-Host "Difference: $difference"

Write-Host "-----------------------"
# Multiplication
$f = 4
$g = 6

$product = $f * $g
Write-Host "Product: $product"

Write-Host "-----------------------"
# Division
$h = 20
$i = 4
$quotient = $h / $i
Write-Host "Quotient: $quotient"
Write-Host "-----------------------"
# Modulus
$j = 22
$k = 5
$remainder = $j % $k
Write-Host "Remainder: $remainder"
Write-Host "-----------------------"